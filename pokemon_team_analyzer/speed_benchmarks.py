@dataclass(frozen=True)
class SpeedBenchmark:
    slug: str
    label: str
    target_speed: int
    source: str


@dataclass(frozen=True)
class SpeedBenchmarkGroup:
    slug: str
    label: str
    benchmarks: tuple[SpeedBenchmark, ...]
    comparison: str = "faster"


@dataclass(frozen=True)
class RegulationSpeedBenchmarkCatalog:
    regulation_id: str
    display_name: str
    notes: str
    groups: tuple[SpeedBenchmarkGroup, ...]


@dataclass(frozen=True)
class BenchmarkSet:
    """A declared reference set whose benchmark speed is *computed*, never hand-entered.

    Benchmarks are generated by running the shared ``compute_stat`` formula over these
    declarations, so the benchmark layer can never drift from the analyzer's own speed
    math. ``nature`` is +1 positive / 0 neutral / -1 negative; ``field_context`` and
    ``item`` apply the same in-battle multipliers the analyzer uses.
    """

    slug: str
    label: str
    base_speed: int
    source: str
    nature: int = 1
    sp: int = CHAMPIONS_MAX_STAT_SPS
    field_context: str | None = None
    item: str | None = None


def _benchmark_speed(declared: BenchmarkSet) -> int:
    speed = compute_stat(declared.base_speed, declared.sp, nature=declared.nature)
    if declared.field_context == "tailwind":
        speed *= 2
    if declared.item == "choice_scarf":
        speed = speed * 3 // 2
    return speed


def _benchmark(declared: BenchmarkSet) -> SpeedBenchmark:
    return SpeedBenchmark(
        slug=declared.slug,
        label=declared.label,
        target_speed=_benchmark_speed(declared),
        source=declared.source,
    )


def _benchmark_group(
    slug: str,
    label: str,
    declared: tuple[BenchmarkSet, ...],
    comparison: str = "faster",
) -> SpeedBenchmarkGroup:
    return SpeedBenchmarkGroup(
        slug=slug,
        label=label,
        benchmarks=tuple(_benchmark(item) for item in declared),
        comparison=comparison,
    )


